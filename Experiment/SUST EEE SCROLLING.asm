
DATA SEGMENT
    PORTA EQU 18H
    PORTB EQU 1AH
    PORTC EQU 1CH
    PORT_CON EQU 1EH
DATA ENDS

CODE SEGMENT
    MOV AX, DATA
    MOV DS, AX

    ORG 0000H
    MOV DX, PORT_CON
    MOV AL, 80h
    OUT DX, AL 
    
    MOV AL,11111111B
    OUT PORTA,AL

START:
  MOV BP,0
CHAR_CHANGE:
MOV CX,10 ; SCROLLING SPEED

CHAR_DELAY:
    MOV SI, 0
    MOV BL,00000001B

L:
    MOV AL,BL
    OUT PORTC,AL
    ROL AL,1
    MOV BL,AL
    MOV AL, SUST[BP+SI]
    OUT PORTB, AL
    CALL DELAY
    INC SI
    CMP SI,8
    JL L
    LOOP CHAR_DELAY
ADD BP,1
CMP BP,72
JE START
JMP CHAR_CHANGE

DELAY:
MOV DX,200 ; VISUAL DELAY
D1:
DEC DX
CMP DX,0
JNE D1
RET

ORG 1000H

SUST DB 11111111B,11111111B,11111111B,11111111B,11111111B,11111111B,11111111B,11111111B ; SPACE
     DB 11111111B,11001101B,10110110B,10110110B,10110110B,11011001B,11111111B,11111111B ;S
     DB 11111111B,10000001B,11111110B,11111110B,11111110B,10000001B,11111111B,11111111B ;U
     DB 11111111B,11001101B,10110110B,10110110B,10110110B,11011001B,11111111B,11111111B ;S
     DB 11111111B,10111111B,10111111B,10000000B,10111111B,10111111B,11111111B,11111111B ;T
     DB 11111111B,11111111B,11111111B,11111111B,11111111B,11111111B,11111111B,11111111B ; SPACE
     DB 11111111B,10000000B,10110110B,10110110B,10110110B,10111110B,11111111B,11111111B ;E
     DB 11111111B,10000000B,10110110B,10110110B,10110110B,10111110B,11111111B,11111111B ;E
     DB 11111111B,10000000B,10110110B,10110110B,10110110B,10111110B,11111111B,11111111B ;E
     DB 11111111B,11111111B,11111111B,11111111B,11111111B,11111111B,11111111B,11111111B ; SPACE
CODE ENDS
END
