
CODE SEGMENT
    ASSUME CS:CODE,DS:CODE,ES:CODE,SS:CODE
    CONT EQU 1FH ; CONTROL REGISTER
    PORTC EQU 1DH   ; PORT C
    PORTB EQU 1BH   ; PORT B
    PORTA EQU 19H   ; PORT A
    MOV AL,80H
    OUT CONT,AL
    
    L1:
    MOV AL,11111111B
    OUT PORTA,AL
    MOV AL,00000001B 
    OUT PORTB,AL
    CALL DELAY 
    MOV AL,00000000B 
    OUT PORTB,AL 
    CALL DELAY
    JMP L1
    
    DELAY:
    MOV CX,1111111111111111B 
    L:
    LOOP L
    RET
    CODE ENDS
END